public class challenge {
    public static void main(String[] args) {
        for(int i = 0; i < 5; i++){
            for(int j = 1; j <= i; j++){
                System.out.print("*");
            }
            System.out.println("*");
        }
        for (int a = 1; a <= 5; a++){
            for (int x = 4; x>=a; x--){
                System.out.print("*");
            }
            System.out.println("*");
        }
    }
    
}
